        <h1>Comment accepter ou refuser les cookies</h1>
        <p>
            Vous disposez de différents moyens pour gérer les cookies :
        </p>
        <ul>
            <li>
                Configurer votre navigateur Internet pour qu’il vous propose ponctuellement l’acceptation ou le
                refus des cookies avant qu’un cookie soit susceptible d’être enregistré dans votre terminal.
            </li>
            <li>
                Configurer votre navigateur Internet pour que certains Cookies soient enregistrés dans votre
                terminal ou bien rejetés, soit systématiquement, soit selon leur émetteur.
            </li>
        </ul>

        <p>
            La configuration de chaque navigateur est différente. Elle est décrite dans le menu d’aide de votre
            navigateur. Voici comment contrôler ou désactiver les cookies :
        </p>

