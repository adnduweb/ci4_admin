            <li>
                Si vous utilisez le navigateur Internet Explorer
                <br />
                Cliquez sur Outils > Options Internet > Général > Paramètres > Afficher les fichiers. Cliquez sur
                l’en-tête de colonne Nom pour trier les fichiers dans l’ordre alphabétique, puis parcourez la liste
                pour trouver les fichiers commençant par le préfixe « Cookie ». Sélectionnez le ou les cookies
                comprenant le nom « moneypitch » et supprimez-les. Fermez la fenêtre et cliquez deux fois sur OK.
            </li>

            <li>
                Si vous utilisez le navigateur Firefox
                <br />
                Allez dans l’onglet Outils du navigateur puis sélectionnez le menu Options puis Vie privée >
                Affichez les cookies. Repérez les fichiers qui contiennent le nom « moneypitch » Sélectionnez-les
                et supprimez-les.
            </li>

            <li>
